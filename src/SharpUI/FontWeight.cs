        /// Normal (Regular).
        /// </summary>
        Normal = 400,

        /// <summary>
        /// Medium.
        /// </summary>
        Medium = 500,

        /// <summary>
        /// Semi-bold (Demi-bold).
        /// </summary>
        SemiBold = 600,

        /// <summary>
        /// Bold.
        /// </summary>
        Bold = 700,

        /// <summary>
        /// Extra-bold (Ultra-bold).
        /// </summary>
        ExtraBold = 800,

        /// <summary>
        /// Black (Heavy).
        /// </summary>
        Black = 900
    }
}
