namespace SharpUI
{
    /// <summary>
    /// An enumeration of text alignments.
    /// </summary>
    public enum TextAlignment
    {
        /// <summary>
        /// Text is aligned to the center.
        /// </summary>
        Center,

        /// <summary>
        /// Text is aligned to the start (left for left-to-right text).
        /// </summary>
        TextStart,

        /// <summary>
        /// Text is aligned to the end (right for left-to-right text).
        /// </summary>
        TextEnd
    }
}
